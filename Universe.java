public class Universe {
  public float curvature;
  public int galaxyPop;
  public long age;
  public final int dimensionality;
  public double habitability;
  
  public Universe(float _curvature, int _galaxyPop, long _age, int _dimensionality, double _habitability) {
    curvature = _curvature;
    galaxyPop = _galaxyPop;
    age = _age;
    dimensionality = _dimensionality;
    habitability = _habitability;
  } 
}
